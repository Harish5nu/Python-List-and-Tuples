# Create tuple
data = (4, 7, 1, 7, 3, 7)

# Count and index
print("Count of 7:", data.count(7))
print("Index of 1:", data.index(1))

# Built-in functions
print("Length:", len(data))
print("Max:", max(data))
print("Min:", min(data))
print("Sum:", sum(data))

# Slicing
print("Sliced Tuple:", data[1:4])
